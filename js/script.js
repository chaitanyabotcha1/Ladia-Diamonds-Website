// VOICE SECTION
// =========================
let voices = [];

// Load available voices
function loadVoices() {
  voices = window.speechSynthesis.getVoices();
}

// Some browsers load voices late
window.speechSynthesis.onvoiceschanged = loadVoices;

// SPEAK FUNCTION
function speakText(text, lang = "en-IN") {
  stopVoice();

  let speech = new SpeechSynthesisUtterance(text);
  speech.lang = lang;
  speech.rate = 0.9;
  speech.pitch = 1;

  let matchedVoice = voices.find(v => v.lang.toLowerCase().includes("en"));

  if (matchedVoice) {
    speech.voice = matchedVoice;
  }

  window.speechSynthesis.speak(speech);
}

// STOP VOICE
function stopVoice() {
  window.speechSynthesis.cancel();
}

// WELCOME VOICE
function speakWelcomeSequence() {
  speakText(
    "Welcome to Ladia Diamonds. For English choose Red, Telugu kosam Blue, Hindi ke liye Black.",
    "en-IN"
  );
}

// SET LANGUAGE
function setLanguage(lang) {
  selectedLanguage = lang;
  localStorage.setItem("selectedLanguage", lang);

  const msg = document.getElementById("selected-language-msg");

  stopVoice();

  if (lang === "en") {
    if (msg) msg.innerText = "You selected English";
    speakText("You selected English. Welcome to Ladia Diamonds.", "en-IN");
  } 
  else if (lang === "te") {
    if (msg) msg.innerText = "మీరు తెలుగు ఎంచుకున్నారు";
    speakText("Meeru Telugu enchukunnaru. Ladia Diamonds ki swagatham.", "en-IN");
  } 
  else if (lang === "hi") {
    if (msg) msg.innerText = "आपने हिंदी चुनी है";
    speakText("Aapne Hindi chuni hai. Ladia Diamonds mein aapka swagat hai.", "en-IN");
  }
}

// PRODUCT DETAILS VOICE
function speakProductDetails(product) {
  if (!product) return;

  stopVoice();

  let text = "";
  let langCode = "en-IN";

  if (selectedLanguage === "en") {
    langCode = "en-IN";
    text = `${product.name}. Price ${product.price} rupees. Metal ${product.metal}. Weight ${product.weight}.`;

    if (product.discount && product.discount !== "0%") {
      text += ` Discount ${product.discount}.`;
    }
  }

  else if (selectedLanguage === "te") {
    langCode = "en-IN";
        text = `${product.name}. Dhara ${product.teluguPrice}. Loha rakam ${product.teluguMetal}. Baruvu ${product.teluguWeight}.`;
    if (product.discount && product.discount !== "0%") {
      text += ` ${product.teluguDiscount } undi.`;
    }
  }

  else if (selectedLanguage === "hi") {
    langCode = "en-IN";
      text = `${product.name}. Keemat ${product.hindiPrice}. Dhatu ${product.hindiMetal}. Wajan ${product.hindiWeight}.`;
    if (product.discount && product.discount !== "0%") {
      text += ` ${product.hindiDiscount } hai.`;
    }
  }

  speakText(text, langCode);
}

