// SEARCH + PAGE LOAD
// =========================
window.addEventListener("DOMContentLoaded", function () {
  loadVoices();

  // SEARCH
  let searchBox = document.querySelector(".icons input");

  if (searchBox) {
    searchBox.addEventListener("keyup", function () {
      let value = searchBox.value.toLowerCase().trim();
      let cards = document.querySelectorAll(".card");

      cards.forEach(function(card) {
        let title = card.querySelector("h3");

        if (!title) return;

        let productName = title.innerText.toLowerCase();

        if (productName.includes(value)) {
          card.style.display = "flex";
        } else {
          card.style.display = "none";
        }
      });
    });
  }

  // Speak welcome only ONCE per browser session
  if (document.querySelector(".language-section")) {
    let alreadySpoken = sessionStorage.getItem("ladiaWelcomeSpoken");

    if (!alreadySpoken) {
      setTimeout(() => {
        speakWelcomeSequence();
        sessionStorage.setItem("ladiaWelcomeSpoken", "yes");
      }, 1500);
    }
  }
});
